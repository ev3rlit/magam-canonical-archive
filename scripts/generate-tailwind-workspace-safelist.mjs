#!/usr/bin/env node

import { readdirSync, readFileSync, writeFileSync } from 'fs';
import { resolve, relative, join } from 'path';
import { fileURLToPath } from 'url';

const repoRoot = resolve(fileURLToPath(new URL('..', import.meta.url)));
const outputPath = resolve(repoRoot, 'app/tailwind.workspace-safelist.cjs');
const defaultTargetDir = resolve(repoRoot, 'examples');

const EXTRA_CLASSES = [
  'bg-rose-500',
  'bg-violet-500',
  'bg-cyan-500',
  'bg-yellow-400',
  'text-black',
  'text-white',
];

function listWorkspaceFiles(rootDir) {
  const files = [];

  const walk = (dir) => {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
      if (entry.name.startsWith('.')) {
        continue;
      }

      const fullPath = join(dir, entry.name);
      if (entry.isDirectory()) {
        walk(fullPath);
        continue;
      }

      if (/\.(js|ts|jsx|tsx|mdx)$/.test(entry.name)) {
        files.push(fullPath);
      }
    }
  };

  walk(rootDir);
  return files.sort();
}

function extractClassTokens(source) {
  const values = [];
  const pattern = /className\s*=\s*(?:"([^"]*)"|'([^']*)'|\{\s*"([^"]*)"\s*\}|\{\s*'([^']*)'\s*\})/gms;

  for (const match of source.matchAll(pattern)) {
    values.push(...match.slice(1).filter(Boolean));
  }

  return values.flatMap((value) => value.split(/\s+/).filter(Boolean));
}

function buildSafelist(targetDir) {
  const files = listWorkspaceFiles(targetDir);
  const classes = new Set(EXTRA_CLASSES);

  files.forEach((filePath) => {
    const source = readFileSync(filePath, 'utf8');
    extractClassTokens(source).forEach((token) => classes.add(token));
  });

  return {
    files: files.map((filePath) => relative(repoRoot, filePath)),
    classes: [...classes].sort(),
  };
}

export function resolveWorkspaceSafelistTargetDir(targetDirArg) {
  return resolve(repoRoot, targetDirArg || process.env.MAGAM_TARGET_DIR || defaultTargetDir);
}

function writeSafelistFile(targetDir, payload) {
  const banner = [
    '// Auto-generated by scripts/generate-tailwind-workspace-safelist.mjs',
    `// Target directory: ${relative(repoRoot, targetDir) || '.'}`,
    `// Class count: ${payload.classes.length}`,
    '',
  ].join('\n');

  const body = `module.exports = ${JSON.stringify(payload.classes, null, 2)};\n`;
  writeFileSync(outputPath, `${banner}${body}`, 'utf8');
}

const targetDirArg = process.argv[2];
const targetDir = resolveWorkspaceSafelistTargetDir(targetDirArg);
const payload = buildSafelist(targetDir);
writeSafelistFile(targetDir, payload);

console.log(`[TailwindSafelist] generated ${payload.classes.length} classes from ${relative(repoRoot, targetDir) || '.'}`);
